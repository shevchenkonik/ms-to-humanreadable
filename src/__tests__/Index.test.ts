import { msToMHS } from '../index';

describe('MS to MHS', () => {
  const numbers: Array<[number, string]> = [
    [0, '0:00:00'],
    [-0, '0:00:00'],
    [999, '0:00:00'],
    [1000, '0:00:01'],
    [-999, '-0:00:00'],
    [-1000, '-0:00:01'],
    [59999, '0:00:59'],
    [60000, '0:01:00'],
    [-59999, '-0:00:59'],
    [-60000, '-0:01:00'],
    [3599999, '0:59:59'],
    [3600000, '1:00:00'],
    [-3599999, '-0:59:59'],
    [-3600000, '-1:00:00'],
    [35999999, '9:59:59'],
    [36000000, '10:00:00'],
    [-35999999, '-9:59:59'],
    [-36000000, '-10:00:00'],
    [86399999, '23:59:59'],
    [86400000, '24:00:00'],
    [123456789, '34:17:36'],
    [-123456789, '-34:17:36'],
    [445401000, '123:43:21'],
    [-445401000, '-123:43:21'],
  ];

  numbers.forEach((i) => {
    it(`${i[0]}`, () => {
      expect(msToMHS(i[0])).toBe(i[1]);
    });
  });
});
